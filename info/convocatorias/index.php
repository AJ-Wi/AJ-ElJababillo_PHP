					<h2>Descripción</h2><br>
					<p>Árbol perennifolio que puede alcanzar los 60 m de altura, su madera es pesada y compacta y sus raíces son bastante superficiales y con contrafuertes, 
					como las de muchas especies forestales de la zona intertropical. El tronco está cubierto de espinas, probablemente desarrolladas como mecanismo de defensa.
					Las hojas ovadas y coriáceas alcanzan los 60 cm de ancho. Es una especie monoica, con flores rojas sin pétalos. Las masculinas surgen en largos racimos, 
					mientras las femeninas surgen solitarias. El fruto es una cápsula similar a una pequeña calabaza de unos 5 a 8 cm de diámetro con dehiscencia explosiva. 
					Los 16 carpelos radiales contienen semillas aplanadas de unos 2 cm de diámetro, encerradas entre dos pequeñas valvas curvas de madera muy dura. 
					Estas semillas maduran al llegar la época de lluvias y al mojarse estallan, produciendo un ruido muy fuerte. 
					Este estallido las puede catapultar a una velocidad de 70 m por segundo.</p><br><br>
					<h2>Usos</h2><br>
